public class WorkflowLogicException extends WorkflowException {

	private static final long serialVersionUID = 1L;

	public WorkflowLogicException() {
	}

	public WorkflowLogicException(String arg0) {
		super(arg0);
	}

	public WorkflowLogicException(Throwable arg0) {
		super(arg0);
	}

	public WorkflowLogicException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
